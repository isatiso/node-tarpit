export type CODES = typeof codes
export type CODES_KEY = keyof typeof codes

export namespace HTTP_STATUS {

    const _whole_set = new Set()
    const _redirect_set = new Set([300, 301, 302, 303, 305, 307, 308])
    const _empty_set = new Set([204, 205, 304])
    const _status_msg = new Map<number, string>()

    export function message_of<C extends number>(code: C): C extends CODES_KEY ? CODES[C] : undefined {
        return _status_msg.get(code) as any
    }

    export function is_redirect(code: number) {
        return _redirect_set.has(code)
    }

    export function is_empty(code: number) {
        return _empty_set.has(code)
    }

    Object.keys(codes).forEach(code => {
        _status_msg.set(+code, codes[code as unknown as CODES_KEY])
        _whole_set.add(+code)
    })
}
